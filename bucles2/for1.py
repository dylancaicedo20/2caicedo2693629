# Este bucle realizara a través del rango de 0 a 10 e imprimirá cada número
for i in range(11):
    print(i)

# Este bucle realizara a través del rango de 1 a 10 e imprimirá cada número
for j in range (1,11):
    print(j)

# Este bucle realizara a través del rango de 0 a 10 con un paso de 2, e imprimirá cada número
for k in range(0,11,2):
    print(k)

# Este bucle realizara a través del rango de 10 a 0 con un paso de -2, e imprimirá cada número
for i in range(10,-1,-2):
    print(i)

# Este bucle realizara a través del rango de 0 a 10 e imprimirá la misma cadena de texto cada vez
for i in range(11):
    print('Amo programar en python')

# Este bucle realizara a través del rango de 1 a 10 e imprimirá el cuadrado de cada número
for i in range(1,11):
    print(i**2)
